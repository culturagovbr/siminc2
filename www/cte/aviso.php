<b>Por que elaborar o Plano de Ações Articuladas (PAR)?</b> 
<p>
	A partir do lançamento do Plano de Desenvolvimento da Educação (PDE), em
	2007, todas as transferências voluntárias e assistência técnica do MEC aos
	municípios, estados e Distrito Federal estão vinculadas à adesão ao
	Compromisso Todos pela Educação e à elaboração do PAR - instrumentos que são
	fundamentais para a melhoria do Ideb.
</p>
<ul>
	<li>
		Assinar o
		<a
			title="Termo de Adesão ao Compromisso Todos pela Educação"
			href="http://portal.mec.gov.br/index.php?option=content&task=view&id=613&Itemid=933&sistemas=1"
		>Termo de Adesão ao Compromisso Todos pela Educação</a>
	</li>
	<li>
		Realizar o diagnóstico da situação da educação no município e em seguida o (PAR)
	</li>
	<li>
		Inserir os dados do PAR no SIMEC - Sistema de Monitoramento do MEC (
		<a
			title=link
			href="http://portal.mec.gov.br/arquivos/pdf/par_simec.pdf"
		>Siga as instruções sobre como inserir os dados no SIMEC</a>)
	</li>
</ul>
<p>
	- &nbsp;Os 1.242 municípios relacionados na
	<a
		title="Resolução CD/FNDE n. 29, de 20 de junho de 2007"
		href="http://portal.mec.gov.br/arquivos/pdf/r29_20062007.pdf"
	>Resolução CD/FNDE n\ 29, de 20 de junho de 2007</a>, serão acompanhados
	pelos consultores do MEC na realização do diagnóstico e na elaboração do PAR.
</p>
<p>
	- Os demais municípios são convidados a elaborar o
	<a
		title="Diagnóstico PAR"
		href="http://portal.mec.gov.br/arquivos/pdf/diagnostico_par.pdf"
	>diagnóstico</a> e o
	<a
		title="PAR "
		href="http://portal.mec.gov.br/arquivos/pdf/par.pdf"
	>Plano de Ações Articuladas</a> PAR de forma autônoma, contando para isso com o
	<a
		title="Manual passo a passo "
		href="http://portal.mec.gov.br/arquivos/pdf/manual_passoapasso.pdf"
	>manual passo a passo</a> e com o
	<a
		title="Guia Prático de Ações"
		href="http://portal.mec.gov.br/arquivos/pdf/guia_pratico_9.pdf"
	>Guia Prático de Ações</a>.
	<br/><br/>
	-&nbsp;Os Estados também são convidados a realizar o
	<a
		title="Diagnóstico dos estados"
		href="http://portal.mec.gov.br/arquivos/pdf/acs_ctpe.pdf"
	>diagnóstico</a> e elaborar o
	<a
		title="PAR Padrão"
		href="http://portal.mec.gov.br/arquivos/pdf/par_padrao.pdf"
	>Plano de Ações Articuladas</a> de forma autônoma, dispondo para isso de um
	Instrumento diagnóstico específico, que se refere tanto ao Ensino Fundamental
	quanto ao Ensino Médio.&nbsp;
	<br/>
</p>
