<p class="P27">DO OBJETO </p>
<p class="P13"> </p>
<p class="P5">

CLÁUSULA PRIMEIRA - O presente
<b>Termo de Cooperação Técnica</b>
tem por objeto a conjugação de esforços entre as partes para a promoção de ações e atividades que contribuam para o processo de desenvolvimento educacional do município, visando a melhoria do Índice de Desenvolvimento da Educação Básica - IDEB.

</p>
<p class="P7"> </p>
<p class="P5"><span class="_T3">Parágrafo único -  O MEC designa
como unidades executoras do presente </span><span class="_T5"><b>Termo
de Cooperação Técnica</b></span><span class="_T3"> o Fundo Nacional
de Desenvolvimento da Educação - FNDE, a Secretaria de Educação Especial
- SEESP, a Secretaria de Educação a Distância - SEED, a Secretaria
de Educação Continuada, Alfabetização e Diversidade - SECAD, a Secretaria de
Educação Profissional e Tecnológica - SETEC e a Secretaria de Educação Básica -
SEB.</span></p>
