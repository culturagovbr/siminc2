<p class="P29">DA VIGÊNCIA </p>
<p class="P29"> </p>
<p class="P34"><span class="T12">CLÁUSULA QUARTA - O presente </span><span
	class="T13"><b>Termo de Cooperação Técnica</b> </span><span class="T12">vigorará pelo
